/* ---- AES-128 encrypt (single block, in-place) ---- */

static uint8_t xtime(uint8_t x) {
    return (uint8_t)((x << 1) ^ (((x >> 7) & 1) * 0x1B));
}

static void sub_bytes(uint8_t *state) {
    for (int i = 0; i < 16; i++)
        state[i] = SBOX[state[i]];
}

static void shift_rows(uint8_t *s) {
    uint8_t t;
    /* row 1: shift left 1 */
    t = s[1]; s[1] = s[5]; s[5] = s[9]; s[9] = s[13]; s[13] = t;
    /* row 2: shift left 2 */
    t = s[2]; s[2] = s[10]; s[10] = t;
    t = s[6]; s[6] = s[14]; s[14] = t;
    /* row 3: shift left 3 (= right 1) */
    t = s[15]; s[15] = s[11]; s[11] = s[7]; s[7] = s[3]; s[3] = t;
}

static void mix_columns(uint8_t *s) {
    for (int c = 0; c < 4; c++) {
        int i = c * 4;
        uint8_t a0 = s[i], a1 = s[i+1], a2 = s[i+2], a3 = s[i+3];
        s[i]   = xtime(a0) ^ xtime(a1) ^ a1 ^ a2 ^ a3;
        s[i+1] = a0 ^ xtime(a1) ^ xtime(a2) ^ a2 ^ a3;
        s[i+2] = a0 ^ a1 ^ xtime(a2) ^ xtime(a3) ^ a3;
        s[i+3] = xtime(a0) ^ a0 ^ a1 ^ a2 ^ xtime(a3);
    }
}

static void add_round_key(uint8_t *state, const uint8_t *rk) {
    for (int i = 0; i < 16; i++)
        state[i] ^= rk[i];
}

static void aes128_encrypt(const uint8_t key[16], const uint8_t in[16], uint8_t out[16]) {
    uint8_t rk[176]; /* 11 round keys × 16 bytes */
    uint8_t state[16];

    /* Key expansion */
    memcpy(rk, key, 16);
    for (int i = 4; i < 44; i++) {
        uint8_t t[4];
        memcpy(t, &rk[(i-1)*4], 4);
        if (i % 4 == 0) {
            uint8_t tmp = t[0];
            t[0] = SBOX[t[1]] ^ RCON[i/4];
            t[1] = SBOX[t[2]];
            t[2] = SBOX[t[3]];
            t[3] = SBOX[tmp];
        }
        for (int j = 0; j < 4; j++)
            rk[i*4+j] = rk[(i-4)*4+j] ^ t[j];
    }

    /* Encrypt */
    memcpy(state, in, 16);
    add_round_key(state, &rk[0]);
    for (int r = 1; r < 10; r++) {
        sub_bytes(state);
        shift_rows(state);
        mix_columns(state);
        add_round_key(state, &rk[r*16]);
    }
    sub_bytes(state);
    shift_rows(state);
    add_round_key(state, &rk[160]);
    memcpy(out, state, 16);
}

/* ---- Public API ---- */

bool gm5byte_compute_key(const uint8_t seed[5], uint8_t key_out[5]) {
    uint8_t tail = seed[4];
    if (tail > 10) return false;

    /* Build plaintext: 11 bytes of 0xFF + 5 bytes of seed */
    uint8_t block[16];
    memset(block, 0xFF, 11);
    memcpy(&block[11], seed, 5);

    /* Encrypt with precomputed AES key */
    uint8_t ct[16];
    aes128_encrypt(T87A_AES_KEYS[tail], block, ct);

    /* MAC = first 5 bytes of ciphertext */
    memcpy(key_out, ct, 5);
    return true;
}
